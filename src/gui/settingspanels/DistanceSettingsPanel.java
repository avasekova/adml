    private void comboBoxDistanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBoxDistanceActionPerformed
        CardLayout card = (CardLayout)panelSettingsDistances.getLayout();
        switch (comboBoxDistance.getSelectedItem().toString()) {
            case "Euclidean distance":
            card.show(panelSettingsDistances, "panelDistanceEuclid");
            break;
            case "Hausdorff distance":
            card.show(panelSettingsDistances, "panelDistanceHausdorff");
            break;
            case "Ichino-Yaguchi distance":
            card.show(panelSettingsDistances, "panelDistanceIchino");
            break;
            case "De Carvalho distance":
            card.show(panelSettingsDistances, "panelDistanceDecarvalho");
            break;
            case "Bertoluzza distance":
            card.show(panelSettingsDistances, "panelDistanceBertoluzza");
            break;
        }
        panelSettingsDistances.repaint();
    }//GEN-LAST:event_comboBoxDistanceActionPerformed


